package kdx.utils

import kotlin.math.absoluteValue
import kotlinx.cinterop.*

object NumberUtils
{
    fun floatToIntBits(value: Float): Int = 0
    fun floatToRawIntBits(value: Float): Int = 0
    fun floatToIntColor(value: Float): Int = 0

    fun intToFloatColor(value: Int): Float = 0f

    fun intBitsToFloat(value: Int): Float = 0f
    fun doubleToLongBits(value: Double): Long = 0L
    fun longBitsToDouble(value: Long): Double = 0.0


}